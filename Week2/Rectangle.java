package Week2;

public class Rectangle {
    private float length = 1.0f;
    private float width = 1.0f;

    // no arguement
    public Rectangle() {
        this.length = 1.0f;
        this.width = 1.0f;
    }

    // para
    public Rectangle(Float length, Float width) {
        this.length = length;
        this.width = width;
    }

}
